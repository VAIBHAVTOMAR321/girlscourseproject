/**
 * Get translated text based on key and current language
 * @param {string} key - Translation key (e.g., 'dashboard.title')
 * @param {string} language - Language code ('en' or 'hi')
 * @returns {string} Translated text or the key if translation not found
 */
export const getTranslation = (key, language = "en") => {
  const lang = language === "hi" ? "hi" : "en";
  return translations[lang][key] || translations["en"][key] || key;
};
